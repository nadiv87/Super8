package interm;

public class CompareTwoValues {

	public static void main(String[] args) {

		int x = 25;

		int y = 35;

		if (x > y)

			System.out.println("x is bigger");

		else if (y > x)

			System.out.println("y is bigger");

		else if (x == y)

			System.out.println("both same");

		else
			System.out.println("error");

	}// end of main

}// end of CompareTwoValues
