package basic;

public class IfelseIfDemo {

	public static void main(String[] args) {

		int x = 35;
		int y = 75;

		if (x > y)
			System.out.println("x is bigger");
		else if (y > x)
			System.out.println("y is bigger");
		else if (x == y)
			System.out.println("both are the same");
		else
			System.out.println("ERROR");

	}// end of main

}// end of class
 